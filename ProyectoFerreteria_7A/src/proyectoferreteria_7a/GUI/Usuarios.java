private void solonumerossinpunto(KeyEvent e) {
    char caracter = e.getKeyChar();
      // Verificar si la tecla pulsada no es un digito
      if(((caracter < '0') || (caracter > '9')) && (caracter != '\b' /*corresponde a BACK_SPACE*/))
      {
         e.consume();  // ignorar el evento de teclado
      }
}
private void sololetrassinpunto(KeyEvent e) {
    char caracter = e.getKeyChar();
      // Verificar si la tecla pulsada no es un digito
      if(((caracter < 'a') || (caracter > 'z')) && (caracter != ' ' /*Si Se Permiten Espacios*/))
      {
         e.consume();  // ignorar el evento de teclado
      }
}
private void solofecha(KeyEvent e) {
    char caracter = e.getKeyChar();
      // Verificar si la tecla pulsada no es un digito
      if(((caracter < '0') || (caracter > '9')) && (caracter != '/' /*corresponde a BACK_SPACE*/))
      {
         e.consume();  // ignorar el evento de teclado
      }
}


