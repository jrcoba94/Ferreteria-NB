    private void JbtnExaminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbtnExaminarActionPerformed
        // TODO add your handling code here:
        
        SelectorImagenes = new JFileChooser();
        if(SelectorImagenes.showOpenDialog(null)== JFileChooser.APPROVE_OPTION)
        {
            RutaArchivo = "";
            Archivo = SelectorImagenes.getSelectedFile();
            RutaArchivo = Archivo.getAbsolutePath();
           //ImageIcon ImagenP= new ImageIcon(((new ImageIcon(RutaArchivo)).getImage()).getScaledInstance(300,300, java.awt.Image.SCALE_SMOOTH));
            ImagenP= new ImageIcon(((new ImageIcon(RutaArchivo)).getImage()).getScaledInstance(JlblImagen.getWidth(),JlblImagen.getHeight(), java.awt.Image.SCALE_SMOOTH));
            JlblImagen.setIcon(ImagenP);
            
        }
        else
        {
            RutaArchivo = "";
            JlblImagen.setIcon(null);
        }
    }//GEN-LAST:event_JbtnExaminarActionPerformed

