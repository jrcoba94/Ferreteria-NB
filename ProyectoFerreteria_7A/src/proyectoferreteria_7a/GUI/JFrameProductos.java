    private void JTableProductosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTableProductosMousePressed
        // TODO add your handling code here:
        //txtDireccion.setText("");
        JbtnAgregar.setEnabled(true);
        JbtnEliminar.setEnabled(true);
        JbtnModificar.setEnabled(true);
        JbtnAgregar.setEnabled(false);
        JtxtFolio.setText(JTableProductos.getValueAt(JTableProductos.getSelectedRow(), 0).toString());
        JtxtProducto.setText(JTableProductos.getValueAt(JTableProductos.getSelectedRow(), 1).toString());
        JtxtNombreProducto.setText(JTableProductos.getValueAt(JTableProductos.getSelectedRow(), 2).toString());
        JtxtPrecio.setText(JTableProductos.getValueAt(JTableProductos.getSelectedRow(), 3).toString());
        JtxtCategoria.setText(JTableProductos.getValueAt(JTableProductos.getSelectedRow(), 4).toString());
        JtxtEstatus.setText(JTableProductos.getValueAt(JTableProductos.getSelectedRow(), 5).toString());
      //cbxCIudad.setSelectedItem(JTableProductos.getValueAt(JTableProductos.getSelectedRow(), 5).toString());
        RutaArchivo = JTableProductos.getValueAt(JTableProductos.getSelectedRow(), 6).toString();
      //txtDireccion.setText(JTableProductos.getValueAt(JTableProductos.getSelectedRow(), 7).toString());
        
        /*RutaArchivo=(tblUsuarios.getValueAt(tblUsuarios.getSelectedRow(), 6).toString());*/
            if (JTableProductos.getValueAt(JTableProductos.getSelectedRow(), 6).toString().equals(""))
            {            
                JlblImagen.setIcon(new ImageIcon (Toolkit.getDefaultToolkit().getImage(ClassLoader.getSystemResource("recursos/CargarFoto.png"))));
            }
            else
            {
                JlblImagen.setIcon(null);
                ImagenP= new ImageIcon(((new ImageIcon((JTableProductos.getValueAt(JTableProductos.getSelectedRow(), 6).toString()))).getImage()).getScaledInstance(JlblImagen.getWidth(),JlblImagen.getHeight(), java.awt.Image.SCALE_SMOOTH));
                JlblImagen.setIcon(ImagenP);
            }      
    }//GEN-LAST:event_JTableProductosMousePressed

